import numpy as np

x = 1.0			#define a float
y = 2.0			#define another float

#trigonometry
print(np.sin(x))			#sinx
print(np.cos(x))			#cosx
print(np.tan(x))			#tanx
print(np.arcsin(x))			#arcs
print(np.arccos(x))			#arcc
print(np.arctan(x))			#arct
print(np.arctan2(x,y))		#arctan(x/y)
print(np.rad2deg(x))		#convert rad to degrees

